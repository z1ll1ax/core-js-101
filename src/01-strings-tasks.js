/**
 * Returns a string that repeated the specified number of times.
 *
 * @param {string} value
 * @param {string} count
 * @return {string}
 *
 * @example
 *   'A', 5  => 'AAAAA'
 *   'cat', 3 => 'catcatcat'
 */
function repeatString(value, count) {
  let newS = '';
  for (let i = 0; i < count; i += 1) {
    newS += value;
  }
  return newS;
  // throw new Error('Not implemented');
}

